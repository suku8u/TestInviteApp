                        <h2 style="margin-top:30px;">Sukumar</h2>
                        <p><span class="dropcap">C</span>an you imagine a stranger offering to help during a move? I knew I met someone special when I first saw Mary. She was bubbly, helpful, kind, & accommodating. I definitely had butterflies in my stomach the very first day when we went dancing.  The idea of having a girlfriend was daunting because it was not something that I was expecting and I realized the barriers for any relationship to progress. It didn't stop me from sending a text message 2 days later. It was just a "Hello". I have never gotten a response to that text message to this day. Later I learned how Mary feels about texting and realized I didn't need to worry.</p>
						<p>After more than two years of friendship and wonderful adventures like eagle watching, paddling, snorkeling, traveling, playing, running, & hiking I felt confident to ask her for a date. I knew her so well by then that I asked her out on a Morning Date for pancakes followed by a Tour of King County Land Fill Facility :). It was more interesting for two of us than a wine bar. Mary dug it too, I think I scored! It's been great journey of understanding love and the inevitable growth the love demands. I feel so blessed to have many wonderful people along my journey to find someone so wonderful.</p>
						<p>One of the first things which made me fall in love with Mary was her lively nature and the way she cares for people. She is a giver to a point of exhaution every day. I learned what it means to live every moment after meeting this girl. She has so many ideas! She has a beautiful mind and I love the way it works. When I am with her I start to think differently too. Her thoughts both complement and challenge mine. We definitely work well together as a team, and our communication has improved since that first text message. I feel like we can be open and honest with each other. I know this is so precious and I feel stoked to have found someone I feel this comfortable with. I can't wait to make more lasting memories with my best friend...</p>
